public class Hermano {
    // atributos
    private String nombre;
    private int edad;


    // constructor con parametros
    public Hermano(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    // getters y setters
    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }
}