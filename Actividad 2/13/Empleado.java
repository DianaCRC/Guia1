public class Empleado {
    private double sueldoActual;

    public Empleado(double sueldoActual) {
        this.sueldoActual = sueldoActual;
    }

    public double getSueldoActual() {
        return sueldoActual;
    }

    public void setSueldoActual(double sueldoActual) {
        this.sueldoActual = sueldoActual;
    }
}